public class Pengiriman {
    String resi;
    String namaBarang;
    float beratBarang;
    int biayaPengiriman;

    Pengiriman(String resi, String namaBarang, float beratBarang, int biayaPengiriman){
        this.resi = resi;
        this.namaBarang = namaBarang;
        this.beratBarang = beratBarang;
        this.biayaPengiriman = biayaPengiriman;
    }
}
